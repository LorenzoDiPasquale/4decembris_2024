""" Insert Name """
name = input("Whats your name: ") # terminālī iegūstiet vārdu

""" Insert age. Also convert age into an Integer value """
age = int(input("How old are you: ")) # saņemt vecumu terminālī

""" Do the math """
year = str((2018 - age)+100) # aprēķiniet kad būsi 100 gadus vecs 

""" Print the result """
print(name + " will be 100 years old in the year " + year) # atbildiet ar savu aprēķināto vecumu
